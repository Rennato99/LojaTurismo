    public void ObtenerCategorias() {
        BLCategory objBLCategory = new BLCategory();
        ArrayList<Category> listCategories = new ArrayList<Category>();
        try {
            objBLCategory.ObtenerProvincias(listCategories);
            DefaultComboBoxModel ModProvincias = new DefaultComboBoxModel();
            for (Category p : listCategories) {
                ModProvincias.addElement(p);
            }
            this.cbmCategory.setModel(ModProvincias);
        } catch (SQLException e) {
            System.err.println("error");
        }
    }

